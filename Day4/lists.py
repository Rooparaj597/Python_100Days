#list is initiate by using [] this square bracket
fruits = ["Cherry", "Apple", "Pear"]

#to add element in this list use var.append(element)
fruits.append("Mango")
print(fruits)

#to remove an element use pop()
fruits.pop(0)
print(fruits)

#to get the specific element in this list use var[0] index value
